package util;

public class TokenJson {

	private String defaultToken = null;
	private String scannerToken = null;
	
	public String getDefaultToken() {
		return defaultToken;
	}
	public void setDefaultToken(String defaultToken) {
		this.defaultToken = defaultToken;
	}
	public String getScannerToken() {
		return scannerToken;
	}
	public void setScannerToken(String scannerToken) {
		this.scannerToken = scannerToken;
	}
	
}
